#include "buzzer.h"
#include "MK60_gpio.h"

/************************************************************************
 *	@brief	蜂鸣器初始化
 *	@param	无
 *	@return	无
 *	@note	无
 ***********************************************************************/
void Buzzer_Init(void)
{
	gpio_init(Buzzer_Pin, GPO, 1);
}

/************************************************************************
 *	@brief	打开蜂鸣器
 *	@param	无
 *	@return	无
 *	@note		无
 ***********************************************************************/
void Buzzer_ON(void)
{
	gpio_set(Buzzer_Pin, 1);
}

/************************************************************************
 *	@brief	关闭蜂鸣器
 *	@param	无
 *	@return	无
 *	@note		无
 ***********************************************************************/
void Buzzer_OFF(void)
{
	gpio_set(Buzzer_Pin, 0);
}

